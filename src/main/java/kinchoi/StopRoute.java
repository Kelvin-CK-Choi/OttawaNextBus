package kinchoi;

public class StopRoute {
    private int stopNo;
    private int routeNo;

    public StopRoute(int stopNo, int routeNo) {
        this.stopNo = stopNo;
        this.routeNo = routeNo;
    }

    public int getStopNo() {
        return stopNo;
    }

    public int getRouteNo() {
        return routeNo;
    }
}
